#include "stdio.h"

int main(void){

    // ARTHEMETIC OPERATORS


    // = + (addition) 
    // = - (Subtraction)
    // = * (Multiplication)
    // = / (Division) 
    // = % (Modulous)
    // = ++ (Incremet)
    // = -- (Decrement)

    int x,y;
    x = 30;
    y = 10;

    printf("%d\n",x+y);
    printf("%d\n",x-y);
    printf("%d\n",x*y);
    printf("%d\n",x/y);
    printf("%d\n",x%y);
    printf("%d\n",x++);
    printf("%d\n",x--);

}